class ValueTooLargeError(Exception):
    pass


A = 'aba'  # input("A:")
B = 'b'  # input("B: ")
if len(A) > 100 or len(B) > 100:
    raise ValueTooLargeError(f"The {A if len(A) > 100 else B} Length is greater than 100")
